/**
 * CODIGO DE EXPLICACION SOBRE QUE ES UNA SECUENCIA Y TIPOS DE SECUENCIAS. ESQUEMAS DE RECORRIDO Y BUSQUEDA.
 * 
 * Lo que manejaremos en este apartado seran los diferentes tipos de algoritmos que podemos utilizar para realizar
 * los ejercicios de secuencias. Algunos deberemos realizarlos mediante la recurrencia como los numeros 
 * de Fibonacci.
 * 
 * Una secuencia es una SUCESION de elementos de un mismo tipo que se acceden linealmente. Hay tres tipos de secuencias:
 *  - Secuencia calculada -> formular la recurrencia que determina el siguiente elemento como un SUMATORIO
 *  - Secuencia explicita -> Como sacar datos de ventas del mes
 *  - Listas
 * 
 * En tipos de secuencias EXPLICITAS manejaremos: 
 *      - Datos introducidos por el teclado o leidos desde un archivo.txt (Todo esto apoyandonos de un centinela o preguntando al usuario)
 * 
 * Para saber el final de la secuencia de LEIDA DE ARCHIVO
 *  - EoF
 *  - Centinela
 * Para saber el final de la secuencia de LEIDA DE TECLADO 
 *  - Preguntar al usuario si quiere introducir un nuevo dato o preguntar al principio cuantos datos se tecleara
 *  - Centinela
 */

/**
 * Las secuencias se pueden tratar a traves de ESQUEMAS, donde existen dos tipos de esquemas:
 * Esquemas de Recorrido y Esquemas de Busqueda.
 * 
 * ESQUEMAS DE RECORRIDO
 * - Aplicar el mismo proceso a todos los elementos de la secuencia
 * - Termina al llegar al final de la secuencia
 * EJEMPLO : Mostrar o operar con los elementos de una secuencia. Indicar si es par o impar cada numero de la secuencia
 * 
 * ESQUEMAS DE BUSQUEDA
 * - Recorrido de la secuencia hasta encontrar el elemento encontrado
 * - Termina al encontrar lo que se busca o al llegar al final de la secuencia(no encontrado)
 * EJEMPLO : Localizar el primero numero que cumpla la condicion como ser mayor al numero 100.
 */